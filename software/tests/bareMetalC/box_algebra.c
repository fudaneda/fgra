#define size 1024
int dd[3];
int block_i[size];
int block[size];
int factor[size];

void hypre_UnionBoxes(){
   int index = 0;
   int join = 0;
   int i_tmp0 = 0;
   int i_tmp1 = 0;
    for (int i = 0; i < 16; i++)
      {
         for (int j = 0; j < 16; j++) 
         {
            join = 0;
            for (int k = 0; k < 8; k++)
            {
               index = (i * 16 + j) * 8 + k;

               if ((join) && (block_i[index] == i_tmp1))
               {
                  block_i[index]  = 0;
                  block_i[i_tmp0] += factor[dd[0]];
               }
               else
               {
                  if (block_i[index])
                  {
                     i_tmp0 = index;
                     i_tmp1 = block_i[index];
                     join  = 1;
                  }
                  else
                     join = 0;
               }
            }
         }
      }
}

