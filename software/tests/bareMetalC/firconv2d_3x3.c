/* Array initialization. */
void init_array()
{
  int i, j;
  for (i = 0; i < NDATA; i++)
  {
    input[i] = i;
  }
  for (i=0;i<SIZE; i++){
    A[i] = i;
  }
}




void result_check()
{
  int i, j;

  for (i = 0; i < NDATA; i++)
  {
    if (output1[i] != output[i]) printf("There is an error in location (%d)[%d, %d]\n", i, output1[i], output[i]);
  }
  for (int i = 1; i < NI-1; i++) {
  	for (int j = 1; j < NJ-1; j++) {
  	    int k = i*NJ + j;
        if (C[k] != B[k]) printf("There is an error in location (%d)[%d, %d]\n", k, C[k], B[k]);
    }
  }
}


