//TYPE stencil2d_res[NI * NJ] __attribute__((aligned(16)));


void stencil2d(){
    int r, c, k1, k2;
    TYPE temp, mul;

    for (r=0; r<NI-2; r++) {
        for (c=0; c<NJ-2; c++) {
            temp = (TYPE)0;
            for (k1=0;k1<3;k1++){
                #pragma unroll
                for (k2=0;k2<3;k2++){
                    mul = stencil2d_filter[k1*3 + k2] * stencil2d_orig[(r+k1)*NJ + c+k2];
                    temp += mul;
                }
            }
            stencil2d_sol[(r*NJ) + c] = temp;
        }
    }
}


